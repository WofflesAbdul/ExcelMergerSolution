/// <summary>
/// Defines the available input file modes.
/// </summary>
public enum TargetFileMode
{
    /// <summary>
    /// Indicates that an existing file will be used as the base.
    /// </summary>
    ExistingFile,

    /// <summary>
    /// Indicates that a new file will be created as the base.
    /// </summary>
    NewFile,
}